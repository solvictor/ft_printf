#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	int		i;
	int		count;
	t_flag	flag;
	va_list	args;

	if (!format)
		return (-1);
	i = 0;
	count = 0;
	va_start(args, format);
	while (format[i])
	{
		if (format[i] == '%' && format[i + 1])
		{
			i++;
			i += handle_flags(format + i, &flag);
			count += handle_conv(format + i, args, &flag);
		}
		else
			count += write(1, &format[i], 1);
		i++;
	}
	va_end(args);
	return (count);
}
